    public NuevoIngredienteView() {
        initComponents();
        List<TipoIngredienteDTO> lista = tipoIngredienteController.getTiposDeIngrediente();
        popularSelectBox(lista);
        List<IngredienteDTO> listaIngredientes = ingredienteController.getIngredientes();
        Object rowData[] = new Object[3];
        DefaultTableModel model = (DefaultTableModel) tablaListaIngredientes.getModel();
            for (IngredienteDTO dto : listaIngredientes) {
                rowData[0] = dto.getNombre();
                rowData[1] = dto.getPrecio();
                rowData[2] = dto.getBaja() == Boolean.TRUE ? "No" : "Si";
                model.addRow(rowData); 
            }  
        this.setLocationRelativeTo(null);
    }
    
    public void popularSelectBox(List<TipoIngredienteDTO> lista) {
        DefaultComboBoxModel model = (DefaultComboBoxModel) jComboBox1.getModel();
        if (lista != null || !lista.isEmpty()) {
        Object[] data;
        Integer i = 0;
            for (TipoIngredienteDTO dto : lista) {
                model.insertElementAt(dto.getNombre(), i);
                i++;
            }
        }
    }

