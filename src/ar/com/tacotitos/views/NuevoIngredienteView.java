    private void confirmarNuevoIngrediente(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarNuevoIngrediente
        // TODO add your handling code here:
        String nombre = jTextField1.getText(); //traer datos de la vista de los input de texto
        String valor = jTextField2.getText();
        String tipoIngrediente = jComboBox1.getSelectedItem().toString(); //Trae dato seleccionado del combo
        if (!tipoIngrediente.equals("--Seleccione--")) {
            Integer result = ingredienteController.nuevoIngrediente(nombre,valor,tipoIngrediente);
            if (result == 1) {
                JOptionPane.showMessageDialog(null, "Ingrediente ingresado correctamente!");
                MainView ni= new MainView();
                ni.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Hubo un error al intentar guardar el ingrediente.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un tipo de ingrediente valido.");
        }
    }//GEN-LAST:event_confirmarNuevoIngrediente

